def add(n1: int | float, n2: int | float) -> int | float:
    return n1 + n2

def subtract(n1: int | float, n2: int | float) -> int | float:
    return n1 - n2

def multiply(n1: int | float, n2: int | float) -> int | float:
    return n1 * n2

def divide(n1: int | float, n2: int | float) -> int | float:
    return n1 / n2